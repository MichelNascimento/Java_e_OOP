package comandos;

//package primeirocomandos //Comando de pacote, sendo opicional.

/* Em um programa vamos ter um método principal (MAIN);
 * que está dentro da classe primeiro programa (CLASS);
 * que faz parte do pacote primeiro programa (PACKAGE)
 */
public class Aula_01_PrimeirosComandos { // Cabeçário da classe.
    public static void main(String[] args) { // Método principal
        System.out.println("Ola, Mundo!");
        /* "System" - Sistema
         * "out"    - Saída
         * "print"  - Imprimir
         * É UM SISTEMA QUE TEM UMA SAÍDA QUE VAI IMPRIMIR
         */ 
    }
    
}
